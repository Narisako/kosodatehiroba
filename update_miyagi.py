# ============================================================
# JSONファイルを更新
# ============================================================
with open('kosodate_kyoten_all.json') as f:
    data = json.load(f)

updated_count = 0
not_found = []

for d in data:
    if d['pref'] != '宮城県':
        continue
    if d.get('addr', '').strip() and d.get('phone', '').strip():
        continue  # Already has data
    
    name = d['name']
    city = d['city']
    
    # Try exact name match first
    r = research.get(name)
    
    # Handle generic names that need city disambiguation
    if not r:
        for key, val in research.items():
            if val.get('city') == city and val.get('original_name') == name:
                r = val
                break
            if val.get('city') == city and key.startswith(name):
                r = val
                break
    
    if r:
        changed = False
        if r.get('addr') and not d.get('addr', '').strip():
            d['addr'] = r['addr']
            changed = True
        if r.get('phone') and not d.get('phone', '').strip():
            d['phone'] = r['phone']
            changed = True
        if changed:
            updated_count += 1
            print(f"  ✅ {city} / {name}")
            print(f"     addr: {d['addr']}")
            print(f"     phone: {d['phone']}")
    else:
        not_found.append(f"{city} / {name}")

print(f"\n更新: {updated_count}件")
if not_found:
    print(f"未マッチ: {len(not_found)}件")
    for nf in not_found:
        print(f"  ❌ {nf}")

# Save
with open('kosodate_kyoten_all.json', 'w') as f:
    json.dump(data, f, ensure_ascii=False, indent=2)
print("\nkosodate_kyoten_all.json を更新しました。")
